package com.example.mvc_radiobutton.model

    // Eredua: kalkulu logika gordetzen du (batuketa eta kenketa)
    class CalculatorModel {

        // Batuketa egiteko metodoa
        fun add(a: Double, b: Double): Double {
            return a + b
        }

        // Kenketa egiteko metodoa
        fun subtract(a: Double, b: Double): Double {
            return a - b
        }
    }